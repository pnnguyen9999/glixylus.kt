<script type="text/javascript">
var list_img_src = '{{$img_list}}'.split(',');
var count = list_img_src.length;
var list_hang = new Array();
for(var i = 0 ; i < count; i++){
	
	var div = document.createElement('div');
	(document.body).appendChild(div);

	var img = document.createElement('img');
	img.src = list_img_src[i];	
	img.style.width = '100px';
	img.style.height = '100px';
	img.setAttribute("id",i);
	img.setAttribute("name","image");
	div.appendChild(img);
}

var img_btt = document.getElementsByName("image");

for(var i = 0 ; i < img_btt.length;i++){
	(function(i){
		var img = document.getElementById(i);
		img.addEventListener("click",function(event){
			mua_hang(img.getAttribute("src"));
		},false);

	})(i);
}

function mua_hang(src){
	src = window.btoa(src);
	list_hang.push(src);
	update_Cookie("list_mat_hang",list_hang.toString());

}

function chuyen_trang(){
	var cookies = document.cookie;
	cookie = cookies.split(";")[0];
	if(list_hang.length !=0){
		document.cookie = "list_mat_hang=" + list_hang.toString();
		window.location.href = "/test/" + list_hang.toString();
	} else {
		if(cookie == "list_mat_hang="){
			alert("ban chua chon mat hang nao gan day");
		} else {
			window.location.href = "/test/" + cookie.substr(14,cookie.length);
		}
	}
	
	
}

function update_Cookie(name,value){
	document.cookie = name + "=" + value;
}

function xoa(){
	document.cookie = "list_mat_hang=";
}
